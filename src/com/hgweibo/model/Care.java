package com.hgweibo.model;

public class Care {
	private Integer id;
	private Integer usersId;
	private Integer careUserId;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getUsersId() {
		return usersId;
	}

	public void setUsersId(Integer usersId) {
		this.usersId = usersId;
	}

	public Integer getCareUserId() {
		return careUserId;
	}

	public void setCareUserId(Integer careUserId) {
		this.careUserId = careUserId;
	}

}
